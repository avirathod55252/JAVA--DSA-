import java.util.*;
public class BinaryLeftShift {
    public static void main (String args[])
    {
        // binary left shift operator << 
        int a =5;
        int b = 1;
        System.out.println(a<<b);
        // formula 
        // /c = 2*b;
        System.out.print(a*(2*b)); 


    }
}
